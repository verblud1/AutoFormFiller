    def _get_screenshot_filename(self, family_data):
        """–ü–æ–ª—É—á–µ–Ω–∏–µ –∏–º–µ–Ω–∏ —Ñ–∞–π–ª–∞ –¥–ª—è —Å–∫—Ä–∏–Ω—à–æ—Ç–∞ –Ω–∞ –æ—Å–Ω–æ–≤–µ –§–ò–û"""
        add_info_text, _, _, _, _, _ = family_data
        
        # –ò–∑–≤–ª–µ–∫–∞–µ–º –§–ò–û –º–∞—Ç–µ—Ä–∏ –∏–∑ —Ç–µ–∫—Å—Ç–∞
        lines = add_info_text.split('\n')
        for line in lines:
            if line.startswith('–ú–∞—Ç—å: '):
                # –ò–∑–≤–ª–µ–∫–∞–µ–º –§–ò–û (—É–±–∏—Ä–∞–µ–º "–ú–∞—Ç—å: " –∏ –¥–∞—Ç—É —Ä–æ–∂–¥–µ–Ω–∏—è)
                mother_info = line[6:]  # –£–±–∏—Ä–∞–µ–º "–ú–∞—Ç—å: "
                # –£–±–∏—Ä–∞–µ–º –¥–∞—Ç—É —Ä–æ–∂–¥–µ–Ω–∏—è (–ø–æ—Å–ª–µ–¥–Ω–∏–µ 10 —Å–∏–º–≤–æ–ª–æ–≤ - –î–î.–ú–ú.–ì–ì–ì–ì)
                if len(mother_info) > 10:
                    mother_name = mother_info[:-10].strip()
                    # –ó–∞–º–µ–Ω—è–µ–º –ø—Ä–æ–±–µ–ª—ã –∏ —Å–ø–µ—Ü–∏–∞–ª—å–Ω—ã–µ —Å–∏–º–≤–æ–ª—ã
                    safe_name = re.sub(r'[\\/*?:"<>|]', '_', mother_name)
                    return safe_name if safe_name else "–Ω–µ–∏–∑–≤–µ—Å—Ç–Ω–æ"
        
        # –ï—Å–ª–∏ –º–∞—Ç–µ—Ä–∏ –Ω–µ—Ç, –∏—â–µ–º –æ—Ç—Ü–∞
        for line in lines:
            if line.startswith('–û—Ç–µ—Ü: '):
                father_info = line[6:]
                if len(father_info) > 10:
                    father_name = father_info[:-10].strip()
                    safe_name = re.sub(r'[\\/*?:"<>|]', '_', father_name)
                    return safe_name if safe_name else "–Ω–µ–∏–∑–≤–µ—Å—Ç–Ω–æ"
        
        return "–Ω–µ–∏–∑–≤–µ—Å—Ç–Ω–æ"
    
